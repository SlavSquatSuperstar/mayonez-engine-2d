package mayonez.graphics.font;

/**
 * Specifies how to horizontally align lines of text within a text label.
 *
 * @author SlavSquatSuperstar
 */
public enum TextAlignment {
    /**
     * Align the left side of the line with the left edge of the label.
     */
    LEFT,
    /**
     * Align the center of the line with the center of the label.
     */
    CENTER,
    /**
     * Align the right side of the line with the right edge of the label.
     */
    RIGHT
}
