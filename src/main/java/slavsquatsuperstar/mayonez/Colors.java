package slavsquatsuperstar.mayonez;

import java.awt.*;

/**
 * A library of common colors.
 * <br>
 * Source: https://htmlcolorcodes.com/color-names/
 * <br>
 * See also: https://en.wikipedia.org/wiki/Web_colors#HTML_color_names
 */
public final class Colors {

    private Colors() {}

    // Rainbow
    public static final Color RED = new Color(255, 0, 0); // Java Red
    public static final Color ORANGE = new Color(255, 165, 0); // HTML Orange
    public static final Color YELLOW = new Color(255, 238, 0); // HTML Bright Yellow
    public static final Color LIME = new Color(0, 255, 0); // Java Green
    public static final Color LIGHT_GREEN = new Color(50, 205, 50); // HTML Lime Green
    public static final Color GREEN = new Color(0, 128, 0); // HTML Green
    public static final Color CYAN = new Color(0, 238, 255); // #00EEFFF
    public static final Color AQUA = CYAN;
    public static final Color LIGHT_BLUE = new Color(30, 144, 255); // HTML Dodger Blue
    public static final Color BLUE = new Color(0, 0, 205); // HTML Medium Blue
    public static final Color PURPLE = new Color(148, 0, 211); // HTML Dark Violet
    // Accents
    public static final Color PINK = new Color(255, 105, 180); // HTML Hot Pink
    public static final Color BROWN = new Color(150, 75, 0); // #964B00
    // Grayscale
    public static final Color WHITE = Color.WHITE;
    public static final Color LIGHT_GRAY = Color.LIGHT_GRAY;
    public static final Color GRAY = Color.GRAY;
    public static final Color DARK_GRAY = Color.DARK_GRAY;
    public static final Color BLACK = Color.BLACK;

}
